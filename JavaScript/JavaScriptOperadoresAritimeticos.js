let n1 = 50
let n2 = 10
let resultado

console.log(`Primeiro Numero ${n1}`)
console.log(`Segundo Numero ${n2}`)

// Adição
resultado = n1 +n2
console.log(`${n1} + ${n2} =  ${resultado}`)

// Subtração
resultado = n1 - n2
console.log(`${n1} + ${n2} =  ${resultado}`)

// Divisão
resultado = n1 /n2
console.log(`${n1} + ${n2} =  ${resultado}`)

// Multiplicação
resultado = n1 * n2
console.log(`${n1} + ${n2} =  ${resultado}`)

// Modulo(Resto da divisão)
resultado = n1 % n2
console.log(`${n1} + ${n2} =  ${resultado}`)

// Potencia
resultado = n1 ** n2
console.log(`${n1} + ${n2} =  ${resultado}`)